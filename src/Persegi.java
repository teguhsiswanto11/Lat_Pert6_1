public class Persegi extends BangunDatar{

    private double sisi;

    public double getSisi() {
        return sisi;
    }

    public void setSisi(double sisi) {
        this.sisi = sisi;
    }

    @Override
    public double hitungkeliling() {
        return 4*sisi;
    }

    @Override
    public double hitungLuas() {
        return sisi*sisi;
    }
}
